# Replacing nested for loops, working towards O(n log n) or better solution
# Initializing tree
name_tree = BinarySearchTree(names_1[1])
# Inserting contents from names_1 into BST
for name_1 in names_1:
    name_tree.insert(name_1)
# Checking names_2 if name is contained in BST, if it is, append to duplicates
for name_2 in names_2:
    if name_tree.contains(name_2):
        duplicates.append(name_2)


end_time = time.time()
print(f"{len(duplicates)} duplicates:\n\n{', '.join(duplicates)}\n\n")
print(f"runtime: {end_time - start_time} seconds")

